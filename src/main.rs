use std::vec::Vec;

//------------------------------------------------------------------------------------------------
#[derive(Debug, Copy, Clone)]
pub enum Rank {
    Ace,
    One,
    Two,
    Three,
    Four,
    Five,
    Six,
    Seven,
    Eight,
    Nine,
    Ten,
    Jack,
    Queen,
    King 
}

//------------------------------------------------------------------------------------------------
#[derive(Debug, Copy, Clone)]
pub enum Suit {
    Club,
    Spade,
    Diamond,
    Heart
}

//------------------------------------------------------------------------------------------------
#[derive(Debug, Copy, Clone)]
pub struct Card {
    pub m_suit : Rank,
    pub m_rank : Suit
}

//------------------------------------------------------------------------------------------------
#[derive(Debug, Clone)]
pub struct Deck {
    m_cards : Vec<Card>
}

//------------------------------------------------------------------------------------------------
impl Deck {
    pub fn cards(&self) -> &Vec<Card> { self.m_cards; }

    pub fn add(&mut self, card : &Card) {
        self.m_cards.push(*card);
    }
}

